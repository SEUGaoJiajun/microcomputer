DATA SEGMENT
STR1    DB 30H,31H,32H,33H,34H
	DB 35H,36H,37H,38H,39H
	DB 40H,41H,42H,43H,44H,45H
COUNT EQU $-STR1
STR2 DB COUNT DUP(00)
DATA ENDS
CODE SEGMENT
	ASSUME DS:DATA,ES:DATA,CS:CODE
BEGIN:  MOV AX,DATA
	MOV DS,AX
	MOV ES,AX
	LEA SI,STR1
	LEA DI,STR2
	MOV CX,COUNT
	CLD
	REP MOVSB
CODE ENDS
	END BEGIN
