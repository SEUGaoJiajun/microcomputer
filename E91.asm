	MOV [BX],AL
	INC BX
	MOV AL,CL
	AND AL,0FH
	OR AL,30H
	MOV [BX],AL
	RET
TOA ENDP
DELAY PROC
	PUSH CX
	PUSH AX
	MOV CX,0FFFFH
GOON:DEC CX
	JNE GOON
